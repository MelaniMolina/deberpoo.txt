
public class Felinos {
    double pesofe;
    String razafe;
    int tamanife;
    String colorfe;
    public Felinos(){
        pesofe = 4.5;
        razafe = "Gato Europeo";
        tamanife = 25;
        colorfe = "Blanco";
    }

    public double getPesofe() {
        return pesofe;
    }

    public void setPesofe(double pesofe) {
        this.pesofe = pesofe;
    }

    public String getRazafe() {
        return razafe;
    }

    public void setRazafe(String razafe) {
        this.razafe = razafe;
    }

    public int getTamanife() {
        return tamanife;
    }

    public void setTamanife(int tamanife) {
        this.tamanife = tamanife;
    }

    public String getColorfe() {
        return colorfe;
    }

    public void setColorfe(String colorfe) {
        this.colorfe = colorfe;
    }
}


